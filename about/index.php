                        <h3>About Hymns</h3>
                        <p>Hymns have been a core part of Christian music for centuries. While some may find hymns boring or simplistic, they are actually a highly developed form of music designed for a specific purpose: congregational singing. Hymns use repetition, rhyming, and relatively simple melodic lines to help congregations quickly learn the music and then be able to sing in worship together. Unlike more contemporary forms of music, which often require the use of instruments or worship leaders, hymns are intended to be sung by the congregation members themselves. Hymns are most commonly sung in unison, but they are often written in four-part harmony. They can be sung a cappella or with the accompaniment of organ, piano, or a whole host of instruments. Hymns are an incredibly versatile music form and have stood the test of time.</p>

                        <h3>About the Asbury Hymn Project</h3>
                        <p>This project was initiated by Nathan Vick and Kellan Kreft after recognizing that awareness of these hymns was declining among their peers, which is especially significant because hymn singing is a major part of Asbury Reunions. After talking with Dr. Schell, Nathan and Kellan also realized that there was no collection or listing of the hymns which have been loved and frequently sung by the Asbury University community throughout the decades. To solve these issues, Nathan and Kellan created the Asbury Hymn Project.</p>
                        <p>Hymns contained in this website were carefully selected by Nathan and Kellan with the guidance of Dr. Mark Schell, who is the chair of Asbury University's Music Department and has extensive knowledge of Asbury hymnody. Beginning with the list of class hymns, they built a comprehensive list of beloved hymns which represent the heart of Asbury University. Hymns were chosen not because of their popularity at any one point but because of their enduring qualities. While the popularity of some hymns has waxed and waned throughout Asbury's history, every hymn on this website is a hymn which has a special place at Asbury.</p>

